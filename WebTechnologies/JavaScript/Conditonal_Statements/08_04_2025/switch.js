//switch case
let floor=5;
switch(floor){
    case 0:
        console.log("You are in ground floor");
        break;
    case 1:
        console.log("You are in first floor");
        break;
    case 2:
        console.log("You are in second floor");
        break;
    case 3:
        console.log("You are in third floor");
        break;
    case 4:
        console.log("You are in fourth floor");
        break;
    case 5:
        console.log("You are in fifth floor");
        break;
    case 6:
        console.log("You are in sixth floor");
        break;
    default:
        console.log("There is no floor");
        
}